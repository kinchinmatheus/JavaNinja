package NivelFacil.TipoDeDados;

public class DadosNaoPrimitivos {
    static void main() {

        String nome = "Naruto Uzumaki";
        String nomeToUpperCase = nome.toUpperCase();
        System.out.println("Esse texto está em CAPSLOCK: " + nomeToUpperCase);
        System.out.println("Esse texto está normal: " + nome);

        String aldeia = "ALDEIA DA FOLHA";
        String aldeiaLowerCase = aldeia.toLowerCase();
        System.out.println(aldeiaLowerCase);


    }

}
